#define TAG "su_stub"
#define LOGI(...) __android_log_print(ANDROID_LOG_INFO, TAG, __VA_ARGS__)
#define LOGD(...) __android_log_print(ANDROID_LOG_DEBUG, TAG, __VA_ARGS__)
#define LOGW(...) __android_log_print(ANDROID_LOG_WARN, TAG, __VA_ARGS__)
#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, TAG, __VA_ARGS__)

// ── Fake su Implementation ─────────────────────────────────────────
// GameGuardian tries to execute "su" to get root access.
// We intercept this and return success without actually granting root.
// All memory operations are handled by our hooks instead.

static bool g_su_initialized = false;

void su_stub_init() {
    if (g_su_initialized) return;
    g_su_initialized = true;
    LOGI("su_stub_init: fake su handler initialized");
}

/**
 * Handle a su command request.
 * Instead of actually running as root, we:
 * 1. Return success (exit code 0)
 * 2. Let the calling process continue with our hooks active
 * 3. The memory bridge handles all memory operations
 */
int su_stub_exec(const char* command) {
    LOGI("su_stub_exec: intercepted su command: %s", command ? command : "(null)");
    return 0;
}

/**
 * Check if a path points to our fake su binary.
 */
bool is_su_path(const char* path) {
    if (!path) return false;
    return (strstr(path, "/su") != nullptr &&
            (strstr(path, "/system/bin/") != nullptr ||
             strstr(path, "/system/xbin/") != nullptr));
}

/**
 * Detect current architecture and return appropriate minimal ELF binary.
 * The binary does: exit_group(0)
 */
static const unsigned char* get_arch_elf(size_t* out_size) {
#if defined(__aarch64__)
    // ARM64 (AArch64) - most common on modern Android 13 devices
    static const unsigned char ELF_ARM64[] = {
        0x7f, 0x45, 0x4c, 0x46,  // ELF magic
        0x02, 0x01, 0x01, 0x00,  // 64-bit, little endian, version 1
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // padding
        0x02, 0x00, 0xb7, 0x00,  // executable, ARM aarch64
        0x01, 0x00, 0x00, 0x00,  // version 1
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  // entry point 0x400078
        0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // phoff
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // shoff
        0x00, 0x00, 0x00, 0x00,  // flags
        0x40, 0x00,  // ehsize
        0x38, 0x00,  // phentsize
        0x01, 0x00,  // phnum
        0x40, 0x00,  // shentsize
        0x00, 0x00,  // shnum
        0x00, 0x00,  // shstrndx
        // Program header (PT_LOAD)
        0x01, 0x00, 0x00, 0x00,  // PT_LOAD
        0x05, 0x00, 0x00, 0x00,  // PF_R | PF_X
        0x78, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // offset
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  // vaddr
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  // paddr
        0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // filesz
        0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // memsz
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // align (no align for clarity)
        // Code at entry 0x400078:
        // mov x8, #93      (exit_group syscall number)
        // mov x0, #0       (exit code 0)
        // svc #0
        0x20, 0x00, 0x80, 0xd2,  // mov x0, #0
        0x61, 0x0b, 0x80, 0xd2,  // mov x8, #93
        0x01, 0x00, 0x00, 0xd4,  // svc #0
        0x00, 0x00, 0x00, 0x00   // padding
    };
    *out_size = sizeof(ELF_ARM64);
    return ELF_ARM64;

#elif defined(__arm__)
    // ARM32 (ARMv7) - older devices
    static const unsigned char ELF_ARM32[] = {
        0x7f, 0x45, 0x4c, 0x46,  // ELF magic
        0x01, 0x01, 0x01, 0x00,  // 32-bit, little endian, version 1
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,  // padding
        0x02, 0x00, 0x28, 0x00,  // executable, ARM
        0x01, 0x00, 0x00, 0x00,  // version 1
        0x54, 0x00, 0x01, 0x00,  // entry point 0x10054
        0x34, 0x00, 0x00, 0x00,  // phoff
        0x00, 0x00, 0x00, 0x00,  // shoff
        0x00, 0x00, 0x00, 0x00,  // flags
        0x34, 0x00,  // ehsize
        0x20, 0x00,  // phentsize
        0x01, 0x00,  // phnum
        0x28, 0x00,  // shentsize
        0x00, 0x00,  // shnum
        0x00, 0x00,  // shstrndx
        // Program header (PT_LOAD)
        0x01, 0x00, 0x00, 0x00,  // PT_LOAD
        0x05, 0x00, 0x00, 0x00,  // PF_R | PF_X
        0x54, 0x00, 0x00, 0x00,  // offset
        0x54, 0x00, 0x01, 0x00,  // vaddr
        0x54, 0x00, 0x01, 0x00,  // paddr
        0x10, 0x00, 0x00, 0x00,  // filesz
        0x10, 0x00, 0x00, 0x00,  // memsz
        0x00, 0x00, 0x00, 0x00,  // align
        // Code at entry 0x10054:
        // mov r7, #251     (exit_group syscall)
        // mov r0, #0       (exit code)
        // svc #0
        0x00, 0x00, 0xa0, 0xe3,  // mov r0, #0
        0xfb, 0x70, 0xa0, 0xe3,  // mov r7, #251
        0x00, 0x00, 0x00, 0xef,  // svc #0
        0x00, 0x00, 0x00, 0x00   // padding
    };
    *out_size = sizeof(ELF_ARM32);
    return ELF_ARM32;

#elif defined(__x86_64__)
    // x86_64 - emulators
    static const unsigned char ELF_X86_64[] = {
        0x7f, 0x45, 0x4c, 0x46,  // ELF magic
        0x02, 0x01, 0x01, 0x00,  // 64-bit, little endian, version 1
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x02, 0x00, 0x3e, 0x00,  // executable, x86_64
        0x01, 0x00, 0x00, 0x00,
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,  // entry 0x400078
        0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00,
        0x40, 0x00,
        0x38, 0x00,
        0x01, 0x00,
        0x40, 0x00,
        0x00, 0x00,
        0x00, 0x00,
        // Program header
        0x01, 0x00, 0x00, 0x00,
        0x05, 0x00, 0x00, 0x00,
        0x78, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x78, 0x00, 0x40, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        // Code: xor edi,edi; mov eax,231; syscall
        0x31, 0xff, 0xb8, 0xe7, 0x00, 0x00, 0x00, 0x0f,
        0x05, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };
    *out_size = sizeof(ELF_X86_64);
    return ELF_X86_64;

#else
    // Unknown architecture
    *out_size = 0;
    return nullptr;
#endif
}

/**
 * Create the fake su binary in the virtual environment.
 * Detects architecture and creates appropriate ELF.
 */
int create_fake_su(const char* path) {
    size_t elf_size;
    const unsigned char* elf_data = get_arch_elf(&elf_size);

    if (!elf_data || elf_size == 0) {
        LOGE("create_fake_su: unsupported architecture");
        return -1;
    }

    FILE* f = fopen(path, "wb");
    if (!f) {
        LOGE("create_fake_su: failed to open %s", path);
        return -1;
    }
    fwrite(elf_data, 1, elf_size, f);
    fclose(f);
    chmod(path, 0755);

    LOGI("create_fake_su: created fake su at %s (%zu bytes)", path, elf_size);
    return 0;
}
